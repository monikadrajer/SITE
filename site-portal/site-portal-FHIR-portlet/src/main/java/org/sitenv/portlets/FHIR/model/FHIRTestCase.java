package org.sitenv.portlets.FHIR.model;

public class FHIRTestCase {
	
	private String testCaseName;
	private String testCaseURI;
	
	/**
	 * @return the testCaseName
	 */
	public String getTestCaseName() {
		return testCaseName;
	}
	/**
	 * @param testCaseName the testCaseName to set
	 */
	public void setTestCaseName(String testCaseName) {
		this.testCaseName = testCaseName;
	}
	/**
	 * @return the testCaseURI
	 */
	public String getTestCaseURI() {
		return testCaseURI;
	}
	/**
	 * @param testCaseURI the testCaseURI to set
	 */
	public void setTestCaseURI(String testCaseURI) {
		this.testCaseURI = testCaseURI;
	}
}
